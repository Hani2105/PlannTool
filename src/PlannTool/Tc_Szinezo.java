    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int szin = jColorChooser1.getColor().getRGB();

        //be kell állítani a táblaadat megfelelő eleméhez.
        //megszamoljuk az info sorokat , hogy ki tudjuk vonni a selected row ból
        int infsor = 0;
        int infoszlop = 0;

        for (int i = 0; i < b.jTable2.getRowCount(); i++) {

            if (b.jTable2.getValueAt(i, 3).toString().equals("Infó")) {

                infsor++;

            }

        }

        for (int i = 0; i < b.jTable2.getColumnCount(); i++) {

            if (b.jTable2.getColumnName(i).equals("Sum: PN,JOB,WS")) {

                infoszlop++;
            }
        }

        try {

            //bejárjuk a táblát és megnézzük , hogy mi van kijelölve
            for (int r = 0; r < b.jTable2.getRowCount(); r++) {

                for (int o = 0; o < b.jTable2.getColumnCount() - infoszlop; o++) {

                    if (b.jTable2.isCellSelected(r, o)) {

                        b.tablaadat[r - infsor][o].szin = szin;
                        if (b.jTable2.getValueAt(r, 3).toString().equals("Terv")) {

                            b.tablaadat[r + 1 - infsor][o].szin = szin;

                        }

                    }

                }

            }

            //  b.tablaadat[b.jTable2.getSelectedRow() - infsor][b.jTable2.getSelectedColumn()].szin = szin;
        } catch (Exception e) {
        }

        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

